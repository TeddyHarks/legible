def _base_intent() -> SlaIntent:
    return SlaIntent(
        declared_var=1000,
        latency_ms=200,
        strictness_multiplier=1.5,
        dependency_depth=2,
        exposure_time_ms=5000,
        provider_stake_ratio=0.9,
        caller_stake_ratio=0.1,
    )


def _run_reference_tests() -> None:
    import sys

    failures = []

    def check(name: str, got, expected, tolerance: float = 0.0):
        if isinstance(expected, float) and tolerance > 0:
            if abs(got - expected) > tolerance:
                failures.append(f"  FAIL {name}: got {got!r}, expected {expected!r}")
        elif got != expected:
            failures.append(f"  FAIL {name}: got {got!r}, expected {expected!r}")

    intent = _base_intent()

    # ?? Vector 1: Empty bundle ????????????????????????????????????????????????
    r = evaluate_sla(intent, SlaAttestationBundle())
    check("v1.outcome", r.outcome.outcome_type, OutcomeType.SLA_PASS)
    check("v1.provider_slash", r.provider_slash, 0)
    check("v1.total_calls", r.total_calls, 0)
    check("v1.confidence", r.attribution_confidence, 1.0)

    # ?? Vector 2: All calls pass ??????????????????????????????????????????????
    r = evaluate_sla(intent, SlaAttestationBundle(
        provider_calls=[
            SlaProviderAttestation("c1", 0, "a", "b", 100, 200, True),
            SlaProviderAttestation("c2", 0, "a", "b", 150, 200, True),
            SlaProviderAttestation("c3", 0, "a", "b", 199, 200, True),
        ]
    ))
    check("v2.outcome", r.outcome.outcome_type, OutcomeType.SLA_PASS)
    check("v2.violation_count", r.violation_count, 0)
    check("v2.total_calls", r.total_calls, 3)

    # ?? Vector 3: Single latency violation, 4 calls, clean caller ????????????
    r = evaluate_sla(intent, SlaAttestationBundle(
        provider_calls=[
            SlaProviderAttestation("c1", 0, "a", "b", 300, 200, True),
            SlaProviderAttestation("c2", 0, "a", "b", 100, 200, True),
            SlaProviderAttestation("c3", 0, "a", "b", 100, 200, True),
            SlaProviderAttestation("c4", 0, "a", "b", 100, 200, True),
        ],
        caller_calls=[
            SlaCallerAttestation("c1", True, False),
        ]
    ))
    check("v3.outcome_type", r.outcome.outcome_type, OutcomeType.SLA_SLASH_PROVIDER)
    check("v3.provider_slash", r.provider_slash, 337)
    check("v3.caller_slash", r.caller_slash, 0)
    check("v3.violation_count", r.violation_count, 1)
    check("v3.total_calls", r.total_calls, 4)
    check("v3.confidence", r.attribution_confidence, 1.0, tolerance=0.0001)

    # ?? Vector 4: Thin evidence ???????????????????????????????????????????????
    r = evaluate_sla(intent, SlaAttestationBundle(
        provider_calls=[
            SlaProviderAttestation("c1", 0, "a", "b", 400, 200, True),
        ],
        caller_calls=[
            SlaCallerAttestation("c1", True, False),
        ]
    ))
    check("v4.outcome_type", r.outcome.outcome_type, OutcomeType.SLA_SHARED_SLASH)
    check("v4.provider_slash", r.provider_slash, 1350)
    check("v4.caller_slash", r.caller_slash, 0)
    check("v4.confidence", r.attribution_confidence, 0.5, tolerance=0.0001)

    # ?? Vector 5: Mixed fault ?????????????????????????????????????????????????
    r = evaluate_sla(intent, SlaAttestationBundle(
        provider_calls=[
            SlaProviderAttestation("c1", 0, "a", "b", 300, 200, True),
            SlaProviderAttestation("c2", 0, "a", "b", 100, 200, True),
            SlaProviderAttestation("c3", 0, "a", "b", 100, 200, True),
            SlaProviderAttestation("c4", 0, "a", "b", 100, 200, True),
            SlaProviderAttestation("c5", 0, "a", "b", 100, 200, True),
        ],
        caller_calls=[
            SlaCallerAttestation("c1", False, False),
        ]
    ))
    check("v5.outcome_type", r.outcome.outcome_type, OutcomeType.SLA_SHARED_SLASH)
    check("v5.provider_slash", r.provider_slash, 135)
    check("v5.caller_slash", r.caller_slash, 15)
    check("v5.confidence", r.attribution_confidence, 0.0, tolerance=0.0001)

    # ?? Vector 6: Severity helper ?????????????????????????????????????????????
    check("v6.sev_337", severity_from_slash(337, 1000), 0.337, tolerance=0.0001)
    check("v6.sev_0", severity_from_slash(0, 1000), 0.0)
    check("v6.sev_1000", severity_from_slash(1000, 1000), 1.0)
    check("v6.sev_clamp", severity_from_slash(2000, 1000), 1.0)
    check("v6.sev_zero_denom", severity_from_slash(500, 0), 0.0)

    # ?? Vector 7: Intent validation ???????????????????????????????????????????
    try:
        validate_intent(_base_intent())
        check("v7.valid_ok", True, True)
    except ValueError:
        failures.append("  FAIL v7.valid_ok: raised unexpectedly")

    for bad_intent, label in [
        (SlaIntent(1000, 200, 1.5, 2, 5000, 0.8, 0.8), "v7.bad_ratios"),
        (SlaIntent(1000, 200, 0.0, 2, 5000, 0.9, 0.1), "v7.bad_strictness"),
        (SlaIntent(1000, 0,   1.5, 2, 5000, 0.9, 0.1), "v7.bad_latency"),
        (SlaIntent(0,    200, 1.5, 2, 5000, 0.9, 0.1), "v7.bad_var"),
    ]:
        try:
            validate_intent(bad_intent)
            failures.append(f"  FAIL {label}: expected ValueError, none raised")
        except ValueError:
            pass  # expected

    # ?? Report ????????????????????????????????????????????????????????????????
    if failures:
        print("REFERENCE VECTOR FAILURES:")
        for f in failures:
            print(f)
        sys.exit(1)
    else:
        print(f"All reference test vectors passed. ({7} vectors)")


if __name__ == "__main__":
    _run_reference_tests()
